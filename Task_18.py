"""
18. Розв'язати лінійне рівняння A · x + B = 0, задане своїми коефіцієнтами A і B (коефіцієнт A НЕ
дорівнює 0)
Скорий Євген 141
"""

def equation():
    a = float(input("Введіть коефіцієнт A - (не дорівнює нулю) = "))
    if a != 0:
        b = float(input("Введіть коефіцієнт B = "))
    else:
        print("Помилка : введіть число, яке не дорівнюе нулю")
    x = float(- (b / a))
    return "X = " + str(x)

print(equation())